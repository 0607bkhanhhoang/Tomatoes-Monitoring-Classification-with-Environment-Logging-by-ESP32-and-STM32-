uint8_t readSoil(void)
{
	uint16_t soil = 0;
	var = val*4096/3.3;
	val+=0.1;
	if (val>=5) val=0;
	HAL_ADC_Start(&hadc1);
	HAL_ADC_PollForConversion(&hadc1, 1000);
	ADC_VAL = HAL_ADC_GetValue(&hadc1);
	HAL_ADC_Stop(&hadc1);
	soil = ((3.3*ADC_VAL/4095 - V25)/Avg_Slope)+25;
	HAL_Delay (100);

	return soil;
}

